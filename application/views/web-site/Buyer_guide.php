                        <h2 class="p-4 text-left" style="font-size: 1.5rem !important;font-weight: bold"
                            id="Link8_target">
                            Annual & Monthly Costs of Buying Property in Turkey
                        </h2>
                        <p class="px-4 text-justify">
                            <b>● How much is Property Tax in Turkey?</b>
                        </p>
                        <p class="px-4 text-justify">
                            <a href="https://www.primepropertyturkey.com/blog/estimated-annual-costs-of-property-ownership-in-turkey"
                               target="_blank">Annual Property Tax </a> is due for all properties (emlak vergisi) in
                            Turkey. This tax
                            differs between a 0.1% and 0.6% rate depending on where you buy and the property type.
                            For example, if you own an apartment in Istanbul, you will be subject to the higher city
                            tax of .2%, calculated by the property’s value. The value is calculated based on nominal
                            value and is calculated by the local council. The nominal value is usually less than the
                            market value.
                        </p>
                        <p class="px-4 text-justify">
                            Property tax returns are filed every four years. This tax is calculated based on the
                            property’s facilities and the size of the plot in square meters. The rates are doubled
                            for a property in major urban areas such as Istanbul.
                        </p>
                        <p class="px-4 text-justify">
                            Property tax is paid directly to local municipalities (belediye) in two installments
                            each year. The first installment is due between March and May, while the second one is
                            due in November, although it can be paid in a lump sum with the first payment. Payment
                            can be made at banks, by check, online, or in cash at the local municipality.
                        </p>
                        <p class="px-4 text-justify" style="font-weight: 700;font-style: italic">
                            * If the tax is not paid on time, the owner of the property pays a penalty fee of
                            approximately 2.5 percent of the amount each month.
                        </p>
                        <p class="px-4 text-justify">
                            Upon property sale, owners are legally required to pay a property transfer tax, which is
                            paid based on the actual sale or purchase price of the property. The sum of
                            approximately 1.5 percent of the value of the real estate needs to be paid by both
                            parties on registration of the transaction at the Land Registry. Prime Property Turkey
                            is here to help every step of the way.
                        </p>
                        <p class="px-4 text-justify">
                            <b>● Property maintenance Fees</b>
                        </p>
                        <p class="px-4 text-justify">
                            Maintenance for property varies from development to development, but the average is
                            about 4-6 TL/ m2 - (If rented, the tenant will assume the payment of these fees).
                        </p>
                        <p class="px-4 text-justify">
                            For more information on annual costs, please refer to our blog on <a
                                    href="https://www.primepropertyturkey.com/blog/estimated-annual-costs-of-property-ownership-in-turkey"
                                    target="_blank">estimated annual costs
                                of property maintenance </a>.
                        </p>
                        <h3 class="p-4 text-left" style="font-size: 1.35rem" id="Link9_target">
                            <b>
                                Income Tax on Turkish Homes
                            </b>
                        </h3>
                        <p class="px-4 text-justify">
                            Income tax is levied on all property owners in Turkey. It is levied from the home
                            earnings. Rental income can be determined by either the actual deduction or the lump sum
                            method
                        </p>
                        <p class="px-4 text-justify">
                            The actual deduction method is when expenses, including water expenses, lighting,
                            insurance, and depreciation cost from the rental income, are deducted.
                        </p>
                        <p class="px-4 text-justify">
                            Under the lump-sum method, taxpayers deduct 25% of their gross income to arrive at a
                            taxable income.
                        </p>
                        <h3 class="p-4 text-left" style="font-size: 1.35rem" id="Link10_target">
                            <b>Capital Gain Tax on Turkish Homes</b>
                        </h3>
                        <p class="px-4 text-justify">
                            Capital Gain Tax is what you pay for an asset’s appreciation during the time you owned
                            it. In Turkey, the capital gain tax is paid when you sell your house within the first
                            five years of purchase, and if you sell it five years after purchase, you are exempted
                            from paying it.
                        </p>
                        <p class="px-4 text-justify">
                            This amount is calculated as a percentage of the profit you make between the stated
                            value of your property when you first bought it and the sale price you declare at the
                            Land Registry (Tapu Office) when you sell.
                        </p>
